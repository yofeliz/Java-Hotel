//Clase Habitacion para almacenar los datos de cada habitación.
//Las habitaciones se almacenan en ListaHabitaciones.

public class Habitacion {
    
    //Número de la habitación.
    int numeroHabitacion;
    //Índice de la categoría de la habitación en la lista de categorías.
    int categoriaHabitacion;
    //Precio de la habitación.
    int precioHabitacion;
    
    public Habitacion() {
        this.numeroHabitacion    = 0;
        this.categoriaHabitacion = 0;
        this.precioHabitacion    = 0;
    }//Constructor
    
}//Habitacion